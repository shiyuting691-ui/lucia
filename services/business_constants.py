def is_valid_department(name: str) -> bool:
    return name in VALID_DEPARTMENTS


def normalize_department(name: str) -> str:
    """把别名纠正为标准角色名；无法识别时原样返回。"""
    if name in VALID_DEPARTMENTS:
        return name
    return DEPARTMENT_ALIASES.get(name, name)
